WORKER_JS = '''/**
 * worker.js
 *
 * Minimal Cloudflare Worker scaffold generated by Boardwalk Playground Studio.
 *
 * Notes:
 * - This file is ESM-style and uses the `fetch` handler signature.
 * - Replace or extend the routes below with your app logic.
 * - For durable bindings, KV, R2, or secrets, reference `env` in the handler.
 */

export default {
  async fetch(request, env, ctx) {
    const url = new URL(request.url);
    // simple routing
    if (url.pathname === "/health") {
      return new Response(JSON.stringify({ status: "ok", service: env.SERVICE_NAME || "worker" }), {
        status: 200,
        headers: { "Content-Type": "application/json" },
      });
    }

    if (url.pathname === "/api/echo" && request.method === "POST") {
      try {
        const body = await request.json();
        return new Response(JSON.stringify({ success: true, echo: body }), {
          status: 200,
          headers: { "Content-Type": "application/json" },
        });
      } catch (err) {
        return new Response(JSON.stringify({ success: false, error: "invalid json" }), { status: 400, headers: { "Content-Type": "application/json" } });
      }
    }

    // default response
    return new Response("Worker for generated app is alive.", { status: 200 });
  }
};
'''

